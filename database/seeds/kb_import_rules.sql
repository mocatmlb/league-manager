Committee at Williamsport.
There will be no waivers, resorting to local rules, or other variation unless granted explicitly from Williamsport.
To administer the tournament properly and scale down thousands of teams to two finalists in the limited time
afforded by the tournament season is an undertaking requiring considerable discipline. Once the tournament
starts, it must proceed without interruption. If protests or disputes occur which cannot be settled by the umpires
or Tournament Director through immediate and concise application of the rules, an appeal must be made
through proper channels promptly to prevent a major blockage or loss of momentum.
Revocation of tournament privileges or forfeiture of a tournament game may be decided only by the Tournament
Committee at Williamsport. Should a problem arise that cannot be resolved while a game is in progress, the
game must be suspended by the Umpire-in-Chief and the problem referred immediately to the Tournament
Director. If not resolved, it must be referred to the Regional Director. If still unresolved, it will be referred to the
Tournament Committee in Williamsport. If the Tournament Committee deems any player to be ineligible, by
league age, residency or school attendance, participation in other programs, or participation in less than eight
(8) [Senior League: No minimum regular season games required] regular season games, it may result in
forfeiture of tournament game(s), and/or suspension or removal of personnel from tournament play, and/or
suspension or removal of personnel or teams from further Little League activities, and/or suspension or

revocation of the local league’s charter. These actions can only be taken by the Tournament Committee in
Williamsport.
The Tournament Committee and the individual Regional Directors may appoint agents to act on their behalf,
and any person so appointed shall have the authority to act as, and exercise the duties of, the Tournament
Committee or the individual Regional Directors.
The Tournament Committee also reserves the right in its sole discretion to impose any penalties
if, in its judgment, any player, manager, coach, umpire, or other volunteer conducts him/herself
in any manner, on or off the field, that does not positively reflect the high standard Little League
holds for sportsmanship, citizenship, and decorum. This includes displays of unsportsmanlike
conduct, “making a travesty of the game,” or violating any rules, regulations, or policies
contained herein during the game, at the game site, at any other location or time related to the
International Tournament. The Committee also reserves the right to impose any penalty the
Committee deems appropriate, if the committee determines action is necessary to correct a
situation brought to its attention, regardless of the source of that information. The decision of
the Tournament Committee is final and binding.
Knowledge of the rules must be guaranteed before a Tournament Director is declared qualified. All Tournament
Directors will undergo a thorough and instructive briefing session prior to taking on their duties, must signify
that they understand the rules, and regardless of personal feelings, they are in full agreement and can interpret
them properly. At the time of the District tournament meeting, it will be required that each league president or
the representative in attendance signify that the league and tournament team managing personnel are
knowledgeable of Tournament Rules and are in full agreement with these conditions.
Selection of Tournament Teams (Recommended Method)
Little League would gain immeasurably in esteem of the public if all tournament teams were selected by the
players themselves. Players relish the challenge of competition, but their anxiety to excel is in balance with an
intuitive respect and admiration for teammate and opponent alike who demonstrate superior ability and skill.
1. It is not required that players be selected for the position they occupy during the regular season. For example, a
pitcher who is also a good outfielder or infielder may be placed on the roster and used in whatever position the
manager deems to be of advantage.
2. Tournament team candidates should be selected upon their playing ability and eligibility. The roster should
include sufficient pitching strength to meet tournament schedules.
3. The following plan was taken from the experience of dedicated volunteers. The principle is to have all
components of a league determine and participate in fair and democratic selection of the tournament team. This
would eliminate many of the complaints, abuses, pressures, and charges of favoritism which are directed toward
the League President. The following groups should each select its tournament team.
1. Group 1 – Players
2. Group 2 – League Officers
3. Group 3 – Team Managers
4. Group 4 – Team Coaches
5. Group 5 – Volunteer Umpires
Every player on the eligible teams is entitled to vote. Each group submits its list of players at a meeting of the
Board of Directors of the league. The names are to be read and counted from each of the groups, and the players
in the order of total votes received will become eligible for the tournament team.

Where more than one player has an equal number of votes to qualify for the last position or positions, final
selection should be made by a majority vote of the Board of Directors at the time of the meeting.
NOTE: Method of selection is to be determined by the local league Board of Directors.

Tournament Organization
Teams
Each chartered league shall be eligible to enter a team. Alternates are not authorized.
NOTE: A league may enter more than one tournament team, provided the teams are deemed fair and balanced,
with the District Administrator’s approval.
Where two or more charters have combined to form a single program, a tournament team must be selected for
each charter composed of players from within its own chartered area. Exceptions can only be made by the
Charter Committee.
Tournament teams and Eligibility Affidavit shall consist of, and must be limited to, a maximum of fourteen (14)
players, one (1) manager, and a maximum of two (2) coaches.
Senior League: Teams and Eligibility Affidavit shall consist of, and must be limited to, a maximum of sixteen
(16) players, one (1) manager, and a maximum of two (2) coaches.
Managers and Coaches
The District Administrator or District Staff shall not serve as manager or coach. The league president and player
agent may be eligible for selection by the local league Board of Directors to coach or manage with written
approval from their respective District Administrator.
As a condition of eligibility for selection as a tournament team manager/coach, the regular season manager/
coach must have also completed the Little League Diamond Leader Training Program (LittleLeague.org/
DiamondLeader). This program must be completed prior to the individual being named a tournament team
manager/coach and before participating in any practices or games. Tournament Team managers/coaches who
have completed the program should be prepared to produce the Little League Diamond Leader completion
certificate if requested by the District Administrator/Tournament Director.
8- to 10- Division: The manager and coach(es) shall be regular season team managers and/or coaches from
the Minor League Division or Little League Baseball (Major) Division.
9- to 11-Year Old Division: The manager and coach(es) shall be regular season team managers and/or
coaches from the Minor League Division or Little League Baseball (Major) Division.
Little League: The manager and coach(es) shall be regular season team managers and/or coaches from the
Little League Baseball (Major) Division or Intermediate (50-70) Division.
Intermediate (50-70) Division: The manager and coach(es) shall be regular season team managers and/or
coaches from the Little League Baseball (Major) Division or Intermediate (50-70) Division.
Junior League: The manager and coach(es) shall be regular season team managers and/or coaches from the
Junior Division or Senior Division.
Senior League: Any individual who meets the requirements of Regulation I(c) 8, 9 & 10, and completed the
required Little League Diamond Leader Training Program, is eligible for selection as a Senior Division
tournament manager or coach.

Managers/Coaches in the Dugout – A maximum of three (3) adults who are named on the affidavit (or
authorized replacements as noted on the affidavit) will be permitted to act as manager/coaches for that game.
The named adults must be listed on the affidavit, or must be authorized temporary replacements as noted on the
affidavit. If there is a third adult listed on the affidavit, that adult is permitted to be in the dugout or on the field
during that game.
NOTE: Base coaches may be adults and/or players provided at least one adult manager or coach remains in the
dugout. See 4.05(b).
Umpires
The Tournament Director shall have full responsibility for providing volunteer Little League umpires for
tournament play. Umpires from leagues involved in the game should not be assigned. The District Administrator
shall not umpire.
There should be at least two umpires in each game. More are recommended when available.
The designated Umpire-in-Chief for each game must be an adult.
Tournament Eligibility Affidavit
It shall be the league president’s responsibility to review and certify the birth records (league age) by viewing the
original birth record and residence or school attendance (as defined by Little League Baseball, Incorporated) of
all players. When the league finally decides on the makeup of the team, names must be entered on the league’s
Eligibility Affidavit. Once the District Administrator certifies the Eligibility Affidavit, the tournament team will
be required to have in its possession:
1. the Eligibility Affidavit;
2. a map, signed, and dated by the league president and District Administrator, showing the actual boundaries of
the league, with locations noted for the residences of the parent or legal guardian (court-appointed) or location
of the school for every participant named on the affidavit;
3. a tournament verification form for each player; NOTE: Players who established “residence” or “school
attendance” for regular season and/or tournament in a prior season using the Tournament
Player Verification form, and can produce the form with proper proofs and signatures, will NOT
need to complete a new Tournament Player Verification form.
4. three or more documents to determine residency of the parent(s) or legal guardian (court-appointed) or a
document to support school attendance/enrollment for each player named on the tournament affidavit;
5. waivers [i.e. II(d), IV(h), Charter Committee, etc.]
6. Original birth records inspected by the District Administrator (or his/her appointee), shall be returned to the
parents or legal guardians and are not to be carried by the tournament team or accompany the tournament
affidavit.
IMPORTANT: Alternates are not authorized. They shall not accompany the team and shall not be
listed on the Eligibility Affidavit.
Eligibility Affidavit must be certified by the District Administrator or his or her designated appointee and
presented by the team manager to the Tournament Director before every game. Each Tournament team must
have twelve (12) eligible players for the District Administrator to certify. EXCEPTION: A District Administrator
may certify the Eligibility Affidavit for those teams that provide a justifiable reason for not having twelve (12)
players.

NOTE: The Eligibility Affidavit becomes official once the team plays its first tournament game.
