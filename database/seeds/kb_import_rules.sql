Tournament Requirement for Non-Citizens
A participant who is not a citizen of the country in which he/she wishes to play, but meets residency or school
attendance requirements as defined by Little League, may participate in that country if:
1. his/her visa allows that participant to remain in that country for a period of at least one year, or;
2. the prevailing laws allow that participant to remain in that country for at least one year, or;
3. the participant has an established bona fide residence in that country for at least two years prior to the start of
the regular season.
Exceptions can only be made by action of the Charter Committee in Williamsport.
Insurance
Accident: A league shall not be accepted for tournament play unless covered by accident insurance, which
includes tournament play. It is strongly recommended that a medical release for each player on the Affidavit be
carried by the team manager.
Liability: Liability Insurance must be carried by the league on whose field tournaments are played as well as
all leagues who participate in the tournament. Minimum coverage of $1,000,000 single limit, bodily injury, and
property damage. The policy must include coverage for claims arising out of athletic participants.
If insurance is purchased locally, a copy of the policy must be on file at Little League
International.
Replacement of Player, Manager, or Coach
Any player, manager, or coach listed on the Eligibility Affidavit who is unable to participate because of injury,
illness, vacation, or other justifiable reason may be replaced by another eligible person. If a player, manager, or
coach is replaced, that person may not be returned to the Eligibility Affidavit. Permanent replacements must be
from the league’s regular season teams and meet all eligibility requirements and shall be recorded and approved
by the District Administrator or Tournament Director in the space provided on the Eligibility Affidavit.
Exception: If a manager or coach is unable to attend a game for a justifiable reason, a
Tournament Director could approve a temporary replacement as outlined in the Little League
Tournament Team Eligibility Affidavit. Temporary replacement of a manager or coach must be
entered on the Eligibility Affidavit. A manager or coach who is ejected from a game may not be

replaced for the team’s next physically played game. (See Rule 4.07)
Playing Equipment
The dimensions and other specifications of all playing equipment used must conform to those set forth in the
Little League, Intermediate (50-70) Division, Junior League, and Senior League Baseball Playing Rules except
for those noted below:
Every member of the team must wear a conventional uniform which includes shirt, pants, socks, and cap. This
may be a regular season uniform.
Each team must provide at least six (6) [7 for Intermediate (50-70) Division/Junior/Senior League]
NOCSAE approved safety helmets with warning labels. The batter, [all base runners, on-deck batter for
Intermediate (50-70) Division/Junior/Senior League], and player base coaches must wear approved
helmets. All male players must wear athletic supporters. Catchers (male) must wear the metal, fiber, or plastic
type cup.
Catchers must wear a mask with (NOCSAE) approved catchers helmet (skull cap type are not acceptable)
and “dangling” type throat guard during practice, infield/outfield, pitcher warm-up, and games.
All catchers must wear approved chest protector and shin guards. Catchers must wear approved long or short
model chest protectors. Shoes with metal cleats or spikes shall not be worn by players, managers, coaches, or
umpires. Intermediate (50-70) Division/Junior/Senior League: Players may wear shoes with metal
spikes.
