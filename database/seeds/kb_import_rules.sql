either the white or colored (orange or green) section of the base. Should, however, the batter-runner reach and
go beyond first base, he/she may only return to the white section of the base. PENALTY: If there is a play on
the batter-runner, and the batter-runner touches only the white portion and the defense appeals prior to the
batter-runner returning to first base, it is treated the same as missing the base. If properly appealed, the batterrunner is out.
