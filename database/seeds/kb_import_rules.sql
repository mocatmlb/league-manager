Selection of Fields
Local leagues selected to host a District, Section, State, Division, Region, or World Series tournament must have
an approved Safety Plan for the current year on the Data Center. Learn more at LittleLeague.org/SAFE.
All games shall be played upon Little League fields approved by the Tournament Director. Exception to this rule
can only be made with the consent of the Region Director.
Fields must be enclosed with an outfield fence. Outfield fences for the Little League Division must be a
maximum of 225 feet from home plate and a minimum of 195 feet; for 8- to 10- and 9- to 11-Divisions, a
maximum of 225 feet and a minimum of 180 feet; for Intermediate (50-70) Division, a maximum of 300 feet
and a minimum of 225 feet; for Junior League, a maximum of 350 feet and a minimum of 250 feet; for Senior
League, a maximum of 420 feet and a minimum of 280 feet. Tournament Directors should not permit portable
outfield fences to exceed 200 feet [250 feet for Intermediate (50-70) Division; and 300 feet for Junior/
Senior League Baseball]. Conventional dirt mounds are approved for tournament play. A Little League
approved artificial mound is permitted at the District level. A request to use an artificial mound for the Section
tournament level and above may be submitted to the Tournament Committee in Williamsport (through the
District Administrator/Tournament Director and Regional Office). An artificial mound must not be used at the
Section level and above unless approved in writing by the Tournament Committee for a specific tournament site/
level, and only for the current year. The Tournament Director or assistant shall judge fitness of the playing field
before the game starts.
8- to 10-Year-Old, 9- to 11-Year-Old, Little League Divisions: The on-deck batter’s position is not
permitted.
NOTE: For additional information about field selection see “Physical Conditions.”
