If a runner is trying to score by a steal or squeeze from third base, note the additional penalty set forth in Rule
7.07. A.R. 2 – If the catcher interferes with the batter before the pitcher delivers the ball, it shall not be
considered interference on the batter under Rule 6.08(c). In such cases, the umpire shall call “Time” and the
pitcher and batter resume the count.
(d) a fair ball touches an umpire or a runner on fair territory before touching a fielder. A.R. – Ball is dead.
Runner(s) who are forced advance and any runners not forced will return to their bases at the time of the pitch.
NOTE: If a fair ball touches an umpire after having passed a fielder other than the pitcher, or having touched a
fielder, including the pitcher, the ball is in play.
6.09 The batter becomes a runner when –
(a) a fair ball is hit;
(b) Majors/Intermediate (50-70) Division/Junior/Senior League: the third strike is not caught,
providing (1) first base is unoccupied or (2) first base is occupied with two out; (NOTE: A batter forfeits his/
her opportunity to advance to first base when he/she enters the dugout or other dead ball area);
(c) a fair ball, after having passed a fielder other than the pitcher, or after having been touched by a fielder,
including the pitcher, shall touch an umpire or runner in fair territory;
(d) a fair fly ball passes over a fence or into the stands at a distance from home base of 165 feet
[Intermediate (50-70) Division: 200 feet; and Junior/Senior League: 250 feet] or more. Such hit
entitles the batter to a home run when all bases have been legally touched. A fair fly ball that passes out of the
playing field at a point less than 165 feet [Intermediate (50-70) Division: 200 feet; and Junior/Senior
League: 250 feet] from home base shall entitle the batter to advance to second base only;
(e) a fair ball, after touching the ground, bounds into the stands; passes through, over, or under a fence;
through or under a scoreboard; or through or under shrubbery or vines on the fence, in which case the batter
and runners shall be entitled to advance two bases;
(f) any fair ball which, either before or after touching the ground, passes through or under a fence, through or
under a scoreboard, through any opening in the fence or scoreboard, through or under shrubbery or vines on
the fence, or which sticks in a fence or scoreboard, in which case the batter and the runners shall be entitled to
two bases;
(g) any bounding fair ball is deflected by the fielder into the stands, or over or under a fence on fair or foul
territory, in which case the batter and all runners shall be entitled to advance two bases;
(h) any fair fly ball is deflected by the fielder into the stands, or over the fence into foul territory, in which case
the batter shall be entitled to advance to second base; but if deflected into the stands or over the fence in fair
territory, the batter shall be entitled to a home run. However, should such a fair fly be deflected at a point less
than 165 feet [Intermediate (50-70) Division: 200 feet; and Junior/Senior League: 250 feet] from
home plate, the batter shall be entitled to two bases only.

Rule 7.00 – The Runner
7.01 A runner acquires the right to an unoccupied base when that runner touches it before being retired. The
runner is then entitled to it until retired or forced to vacate it for another runner legally entitled to that base. If a
runner legally acquires title to a base, and the pitcher assumes his/her position on the pitcher’s plate, the runner
may not return to a previously occupied base.
7.02 In advancing, a runner shall touch first, second, third, and home base in order. If forced to return, the runner
shall retouch all bases in reverse order, unless the ball is dead under any provision of Rule 5.09. In such cases, the
runner may go directly to the original base.
7.03 Two runners may not occupy a base, but if, while the ball is alive, two runners are touching the base, the
following runner shall be out when tagged. The preceding runner is entitled to the base.
(a) If two runners are on a base and both are tagged, then the lead runner is out if forced.
7.04 Each runner, other than the batter, may, without liability to be put out, advance one base when –
(a) the batter’s advance without liability to be put out forces the runner to vacate a base, or when the batter
hits a fair ball that touches another runner or the umpire before such ball has been touched by, or has passed a
fielder, if the runner is forced to advance, or in Intermediate (50-70) Division/Junior/Senior League,
there is a balk;
(b) a fielder, after catching a fly ball, falls into a stand, falls across ropes into a crowd when spectators are on
the field, or falls into any other dead-ball areas;
(c) Intermediate (50-70) Division/Junior/Senior League: while the runner is attempting to steal a
base, the batter is interfered with by the catcher or any other fielder. NOTE: When a runner is entitled to a
base without liability to be put out, while the ball is in play, or under any rule in which the ball is in play after
the runner reaches an entitled base, and the runner fails to touch the base to which that runner is entitled
before attempting to advance to the next base, the runner shall forfeit the exemption from liability to be put out
and may be put out by tagging the base or by tagging the runner before that runner returns to the missed base.
A.R. – A runner forced to advance without liability to be put out, may advance past the base to which he/she is
entitled at his/her own risk.
7.05 Each runner including the batter-runner may, without liability to be put out, advance –
(a) to home base scoring a run, if a fair ball goes out of the playing field in flight and the runner touches all
bases legally; or if a fair ball which, in the umpire’s judgment, would have gone out of the playing field in flight
[165 feet from home plate; Intermediate (50-70) Division: 200 feet; and Junior/Senior League:
250 feet], is deflected by the act of a fielder in throwing a glove, cap, or any article of apparel;
(b) three bases, if a fielder deliberately touches a fair ball with a cap, mask, or any part of that fielder’s uniform
detached from its proper place on the person of said fielder. The ball is in play and the batter may advance to
home plate at the batter’s peril;
(c) three bases, if a fielder deliberately throws a glove and touches a fair ball. The ball is in play and the batter
may advance to home base at that batter’s peril;
(d) two bases, if a fielder deliberately touches a thrown ball with a cap, mask, or any part of the uniform
detached from its proper place on the person of said fielder. The ball is in play;
(e) two bases, if a fielder deliberately throws a glove at and touches a thrown ball. The ball is in play;

(f) two bases, if a fair ball bounces or is deflected into the stands outside the first or third base foul line; if it
goes through or under a field fence; through or under a scoreboard; through or under shrubbery or vines on
the fence; or if it sticks in such fence, scoreboard, shrubbery, or vines;
(g) two bases when, with no spectators on the playing field, a thrown ball goes into the stands; into a bench
(whether or not the ball rebounds into the field); over, under, or through a field fence; on a slanting part of the
screen above the backstop; or remains in the meshes of a wire screen protecting spectators. The ball is dead.
When such wild throw is the first play by an infielder, the umpire, in awarding such bases, shall be governed by
the position of the runners at the time the ball was pitched; in all other cases the umpire shall be governed by
the position of the runners at the time the wild throw was made; A.R. – If all runners, including the batterrunner, have advanced at least one base when an infielder makes a wild throw on the first play after the pitch,
the award shall be governed by the position of the runners when the wild throw was made.
(h) one base, if a ball, pitched to the batter, or thrown by the pitcher from the position on the pitcher’s plate to
a base to catch a runner, goes into a stand or a bench; or over, through, or lodges in a field fence or backstop.
The ball is dead;
(i) one base, if the batter becomes a runner on a ball four when the pitch passes the catcher and lodges in the
umpire’s mask or paraphernalia;
Majors/Intermediate (50-70) Division/Junior/Senior League: one base, if the batter becomes a
runner on a ball four or strike three when the pitch passes the catcher and lodges in the umpire’s mask or
paraphernalia. NOTE 1: If the batter becomes a runner on a wild pitch which entitles the runners to advance
one base, the batter-runner shall be entitled to first base only but can advance beyond first base at their own
risk if the ball stays in play. NOTE 2: In Tee Ball, the runner or runners will be permitted to advance at their
own risk on an overthrow that remains in play, but not more than one base.
(j) one base, if a fielder deliberately touches a pitched ball with his/her cap, mask, or any part of his/her
uniform detached from its proper place on his/her person. The ball is in play, and the award is made based on
the position of the runner at the time the ball was touched.
7.06 When the obstruction occurs, the umpire shall call or signal “Obstruction.”
(a) If a play is being made on the obstructed runner, or if the batter-runner is obstructed before touching first
base, the ball is dead and all runners shall advance without liability to be put out, to the bases they would have
reached, in the umpire’s judgment, if there had been no obstruction. The obstructed runner shall be awarded at
least one base beyond the base last legally touched by such runner, before the obstruction. Any preceding
runners forced to advance by the award of bases as the penalty for obstruction shall advance without liability to
be put out;
(b) If no play is being made on the obstructed runner, the play shall proceed until no further action is possible.
The umpire shall then call “Time” and impose such penalties, if any, as in that umpire’s judgment will nullify
the act of obstruction. NOTE 1: When the ball is not dead on obstruction and an obstructed runner advances
beyond the base which, in the umpire’s judgment, the runner would have been awarded because of being
obstructed, the runner does so at his/her own risk and may be tagged out. This is a judgment call. NOTE 2: If
the defensive player blocks the base (plate) or base line clearly without possession of the ball, obstruction shall
be called. The runner is safe and a delayed dead ball shall be called.
7.07 Intermediate (50-70) Division/Junior/Senior League: If, with a runner on third base and trying to
score by means of a squeeze play or a steal, the catcher or any other fielder steps on, or in front of home base
without possession of the ball, or touches the batter or the bat, the pitcher shall be charged with a balk, the batter
shall be awarded first base on the interference and the ball is dead.

7.08 Any runner is out when –
(1) running more than three feet away from his/her base path to avoid being tagged, unless such action is
to avoid interference with a fielder fielding a batted ball. A runner’s base path is established when the tag
attempt occurs and is a straight line from the runner to the base to which he/she is attempting to reach; or
(2) after touching first base, the runner leaves the baseline, obviously abandoning all effort to touch the
next base; or
(3) the runner does not slide or attempt to get around a fielder who has the ball and is waiting to make the
tag; or A.R. – There is no “must slide rule.”
(4) Little League (Major) and below only: the runner slides head first while advancing.
(b) intentionally interferes with a thrown ball; or hinders a fielder attempting to make a play on a batted ball
(NOTE: A runner who is adjudged to have hindered a fielder who is attempting to make a play on a batted ball
is out whether it was intentional or not);
(c) that runner is tagged, when the ball is alive, while off a base; EXCEPTION: A batter-runner cannot be
tagged out after overrunning or over sliding first base if said batter-runner returns immediately to the base.
A.R. 1 – This includes a batter-runner who overruns first after being awarded a base on balls. A.R. 2 – If the
impact of a runner breaks a base loose from its position, no play can be made on that runner at that base if the
runner had reached the base safely. A.R. 3 – If a base is dislodged from its position during a play, any following
runner on the same play shall be considered as touching or occupying the base if, in the umpire’s judgment,
that runner touches or occupies the dislodged bag, or the point marked by the original location of the dislodged
bag.
(d) failing to retouch the base after a fair or foul ball is legally caught before that runner, or the base, is tagged
by a fielder. The runner shall not be called out for failure to retouch the base after the first following pitch, or
any play or attempted play. This is an appeal play. NOTE: Base runners can legally retouch their base once a
fair ball is touched in flight and advance at their own risk if a fair or foul ball is caught.
(e) failing to reach the next base before a fielder tags said runner or the base after that runner has been forced
to advance by reason of the batter becoming a runner. However, if a following runner is retired on a force play,
the force is removed and the runner must be tagged to be put out. The force is removed as soon as the runner
touches the base to which that runner is forced to advance, and if oversliding or overrunning the base, the
runner must be tagged to be put out. However, if the forced runner, after touching the next base, retreats for
any reason towards the base last occupied, the force play is reinstated and the runner can again be put out if
the defense tags the base to which the runner is forced;
(f) touched by a fair ball in fair territory before the ball has touched or passed an infielder. The ball is dead and
no runner may score, no runners may advance, except runners forced to advance; EXCEPTION: If a runner is
touching a base when touched by an Infield Fly, that runner is not out, although the batter is out. NOTE 1: If a
runner is touched by an Infield Fly when not touching a base, both runner and batter are out. NOTE 2: If two
runners are touched by the same fair ball, only the first one is out because the ball is instantly dead.
(g) attempting to score on a play in which the batter interferes with the play at home base before two are out.
With two out, the interference puts the batter out and no score counts;
(h) passes a preceding runner before such runner is out;
(i) after acquiring legal possession of a base, the runner runs the bases in reverse order for the purpose of
confusing the defense or making a travesty of the game. The umpire shall immediately call “Time” and declare
the runner out;

(j) failing to return at once to first base after overrunning or oversliding that base. If attempting to run to
second the runner is out when tagged. If, after overrunning or oversliding first base the runner starts toward
the dugout, or toward a position, and fails to return to first base at once, that runner is out, on appeal, when
said runner or the base is tagged;
(k) in running or sliding for home base, the runner fails to touch home base and makes no attempt to return to
the base, when a fielder holds the ball in hand, while touching home base, and appeals to the umpire for the
decision. (NOTE: This rule applies only where the runner is on the way to the bench and a fielder would be
required to chase the runner to tag him/her. It does not apply to the ordinary play where the runner misses the
plate and then immediately makes an effort to touch the plate before being tagged. In that case, the runner
must be tagged.)
7.09 It is interference by a batter or runner when –
(a) the batter hinders the catcher in an attempt to field the ball;
(b) the batter intentionally deflects the course of a foul ball in any manner;
(c) before two are out and a runner on third base, the batter hinders a fielder in making a play at home base;
the runner is out;
(d) any member or members of the offensive team stand or gather around any base to which a runner is
advancing, to confuse, hinder, or add to the difficulty of the fielders. Such runner shall be declared out for the
interference of a teammate or teammates;
(e) any batter or runner who has just been retired hinders or impedes any following play being made on a
runner. Such runner shall be declared out for the interference of a teammate;
(f) if, in the judgment of the umpire, a base runner willfully and deliberately interferes with a batted ball or a
fielder in the act of fielding a batted ball with the obvious intent to break up a double play, the ball is dead. The
umpire shall call the runner out for interference and also call out the batter-runner because of the action of the
runner. In no event may bases be run or runs scored because of such action by a runner;
(g) if, in the judgment of the umpire, a batter-runner willfully and deliberately interferes with a batted ball or a
fielder in the act of fielding a batted ball, with the obvious intent to break up a double play, the ball is dead; the
umpire shall call the batter-runner out for interference and shall also call out the runner who advanced closest
to the home plate regardless where the double play might have been possible. In no event shall bases be run
because of such interference;
(h) in the judgment of the umpire, the base coach at third base or first base, by touching or holding the runner,
physically assists that runner in returning to or leaving third base or first base; A.R. – When a play is being
made on the assisted runner, the runner is out and all runners return to the bases occupied at the time of the
interference (dead ball). If no play is being made on the assisted runner, the runner is out and play continues
(delayed dead ball).
(i) with a runner on third base, the base coach leaves the box and acts in any manner to draw a throw by a
fielder;
(j) the runner fails to avoid a fielder who is attempting to field a batted ball, or intentionally interferes with a
thrown ball, provided that if two or more fielders attempt to field a batted ball, and the runner comes in contact
with one or more of them, the umpire shall determine which fielder is entitled to the benefit of this rule, and
shall not declare the runner out for coming in contact with a fielder other than the one the umpire determines
to be entitled to field such a ball;
(k) a fair ball touches the batter or runner in fair territory before touching a fielder. If a fair ball goes through
or by an infielder and touches a runner immediately back of said infielder or touches the runner after having

been deflected by a fielder, the umpire shall not declare the runner out for being touched by a batted ball. In
making such decision, the umpire must be convinced that the ball passed through or by the infielder and that
no other infielder had the chance to make a play on the ball. If in the judgment of the umpire, the runner
deliberately and intentionally kicks such a batted ball on which the infielder had missed a play, then the runner
shall be called out for interference. PENALTY FOR INTERFERENCE: The runner is out and the ball is
dead.
7.10 Any runner shall be called out, on appeal, when –
(a) after a fly ball is caught, the runner fails to retouch the base before said runner or the base is tagged
(NOTE: “Retouch” in this rule, means to tag up and start from a contact with the base after the ball is caught.
A runner is not permitted to take a flying start from a position in back of, and not touching, the base);
(b) with the ball in play, while advancing or returning to a base, the runner fails to touch each base in order
before said runner, or a missed base, is tagged; A.R. – (1) No runner may return to touch a missed base after a
following runner has scored. (2) When the ball is dead, no runner may return to touch a missed base or one
abandoned after said runner has advanced to and touched a base beyond the missed base. Play A – Batter hits
the ball out of the park, or hits a ground rule double, and misses first base (ball is dead). The runner may
return to first base to correct the mistake before touching second. But if the runner touches second, he/she
may not return to first and if the defensive team appeals, the runner is declared out at first. (Appeal play.)
Play B – Batter hits a ground ball to shortstop, who throws wild into the stands (ball is dead). Batter-runner
misses first base but is awarded second base on the overthrow. Even though the umpire has awarded the
runner second base on the overthrow, the runner must touch first base before proceeding to second base.
(Appeal play.)
(c) the runner overruns or overslides first base and fails to return to the base immediately, and said runner or
the base is tagged;
(d) the runner fails to touch home base and makes no attempt to return to that base, and home base is tagged.
NOTE: A runner forfeits his/her opportunity to return to home base when he/she enters the dugout or other
dead ball area.
Any appeal under this rule must be made before the next pitch, or any play or attempted play. No appeal can be
made if the ball is dead. If the violation occurs during a play which ends a half-inning, the appeal must be made
before all the defensive players have left fair territory on their way to the bench or dugout. EXCEPTION: If an
otherwise proper appeal is being made by a player who has to go into foul territory to retrieve the ball in order
to make an appeal or if the appeal is being made by the catcher (who may never have been in fair territory at
all), the appeal will be adjudged to have been properly executed. An appeal is not to be interpreted as a play or
an attempted play. Successive appeals may not be made on a runner at the same base. If the defensive team on
its first appeal errs, a request for a second appeal on the same runner at the same base shall not be allowed by
the umpire. (Intended meaning of the word “err” is that the defensive team in making an appeal threw the ball
into a dead ball area. For example, if the pitcher threw to first base to appeal and threw the ball into the stands,
no second appeal would be allowed.) NOTE 1: Appeal plays may require an umpire to recognize an apparent
“fourth out.” If the third out is made during a play in which an appeal play is sustained on another runner, the
appeal play decision takes precedence in determining the out. If there is more than one appeal during a play
that ends a half-inning, the defense may elect to take the out that gives it the advantage. For the purposes of
this rule, the defensive team has “left the field” when all players have left fair territory on their way to the bench
or dugout. NOTE 2: If a pitcher makes an illegal pitch [a balk in Intermediate (50-70) Division/Junior/
Senior League] when making an appeal, such act shall be a play. An appeal should be clearly intended as an
appeal, either by a verbal request by the player or an act that unmistakably indicates an appeal to the umpire. A

player, inadvertently stepping on the base with a ball in hand, would not constitute an appeal. The ball must be
live and in play.
7.11 The players, coaches, or any member of an offensive team shall vacate any space (including both dugouts)
needed by a fielder who is attempting to field a batted or thrown ball. PENALTY: Interference shall be called and
the batter or runner on whom the play is being made shall be declared out.
7.12 Unless two are out, the status of a following runner is not affected by a preceding runner’s failure to touch or
retouch a base. If, upon appeal, the preceding runner is the third out, no runners following the preceding runner
shall score. If such third out is the result of a force play, neither preceding nor following runners shall score.
7.13 Little League (Major) and Minor League: When a pitcher is in contact with the pitcher’s plate and in
possession of the ball and the catcher is in the catcher’s box ready to receive delivery of the ball, base runners shall
not leave their bases until the ball has been delivered and has reached the batter. NOTE: Tee Ball: Base runners
must stay in contact with the base until the ball is hit. When players have advanced as far as possible without being
put out or having been retired, the umpire shall call “time” and place the ball on the tee. The violation by one base
runner shall affect other base runners –
(a) when a base runner leaves the base before the pitched ball has reached the batter and the batter does not
hit the ball, the runner is permitted to continue. If a play is made on the runner and the runner is out, the out
stands. If said runner reaches safely the base to which the runner is advancing, that runner must be returned to
the base occupied before the pitch was made, and no out results;
(b) when a base runner leaves the base before the pitched ball has reached the batter and the batter hits the
ball, the base runner or runners are permitted to continue. If a play is made and the runner or runners are put
out, the out or outs will stand. If not put out, the runner or runners must return to the original base or bases or
to the unoccupied base nearest the one that was left; In no event shall the batter advance beyond first base on a
single or error, second base on a double or third base on a triple. The Umpire-in-Chief shall determine the base
value of the hit ball.
(c) when any base runner leaves the base before the pitched ball has reached the batter and the batter bunts,
hits a ball within the infield or advances on an uncaught third strike, no run shall be allowed to score. If three
runners were on the bases and the batter reaches first base safely, each runner shall advance to the base
beyond the one they occupied at the start of the play except the runner who occupied third base, that runner
shall be removed from the base without a run being scored. NOTE: See exceptions following this rule.
EXCEPTION: If at the conclusion of the play there is an open base, paragraphs (a) and (b) will apply
EXAMPLES:
(1) Runner on first leaves too soon, batter reaches first safely, runner goes to second.
(2) Runner on second leaves too soon, batter reaches first safely, runner returns to second.
(3) Runner on third leaves too soon, batter reaches first safely, runner returns to third.
(4) Runner on first leaves too soon, batter hits clean double, runner goes to third only.
(5) Runner on second leaves too soon, batter hits clean double, runner goes to third only.
(6) Runner on third leaves too soon, batter hits clean double, runner returns to third.
(7) All runners on base will be allowed to score when the batter hits a clean triple or home run, regardless of
whether any runner left too soon.
(8) Runners on first and second, either leaves too soon, batter reaches first safely, runners go to second and
third.

(9) Runners on first and second, either leaves too soon, batter hits clean double, runner on first goes to third,
runner on second scores.
(10) Runners on first and third, either leaves too soon, batter reaches first safely, runner on first goes to
second, runner on third remains there.
(11) Runners on first and third, either leaves too soon, batter hits a clean double, runner on first goes to third,
runner on third scores.
(12) Runners on second and third, either leaves too soon, batter reaches first safely, neither runner can
advance.
(13) Runners on second and third, either leaves too soon, batter hits a clean double, runner on third scores,
runner on second goes to third.
(14) Runners on first, second, and third, any runner leaves too soon, batter hits clean double, runners on
second, third score, runner on first goes to third.
(15) Bases full, any runner leaves too soon, batter reaches first safely on any ball bunted or hit within the
infield, all runners advance one base except runner advancing from third. Runner advancing from third is
removed, no run is scored and no out charged. If on the play, a putout at any base results in an open base,
runner who occupied third base returns to third base.
(16) Bases full, any runner leaves too soon, batter received a base on balls or is hit by a pitch, each runner will
advance one base and a run will score. NOTE 1: When an umpire detects a base runner leaving the base too
soon, that umpire shall drop a signal flag or handkerchief immediately to indicate the violation. NOTE 2: For
purpose of these examples, it is assumed that the batter runner remains at the base last acquired safely. NOTE
3: Tee Ball: Base runners must stay in contact with the base until the ball is hit. When players have advanced
as far as possible without being put out or having been retired the umpire shall call “time” and place the ball on
the tee.
(a) Once each inning a team may utilize a player who is not in the batting order as a special pinch-runner for
any offensive player. A player may only be removed for a special pinch-runner one time during a game. The
player for whom the pinch-runner runs is not subject to removal from the lineup. If the pinch-runner remains
in the game as a substitute defensive or offensive player, the player may not be used again as a pinch-runner
while in the batting order. However, if removed for another substitute that player or any player not in the lineup, is again eligible to be used as a pinch-runner. A local league may adopt Tournament Rule 3(d) for 7.14.
NOTE: Does not apply if the local league adopts the continuous batting order. See Rule 4.04.
(b) Courtesy Runner: A local league may permit a “courtesy runner” for the catcher and/or pitcher of record
when there are two (2) outs. NOTE 1: If the traditional batting order is used, a player whose name is on the
team’s batting order may not become a substitute runner for another member of the team. Neither the pitcher
nor the catcher is subject to removal from the line-up. The same courtesy runner may not run for both the
pitcher and catcher at any time during the game. NOTE 2: If the continuous batting order is used, the
“courtesy runner” may be in the team’s batting order and must be the player in the batting order who made the
last out. NOTE 3: If the pitcher and catcher of record are on base with two outs, the offense may use a courtesy
runner for both the pitcher and catcher of record at the same time. When assigning courtesy runners in such
situations, the player who is scheduled to bat soonest shall be placed on the base closest to scoring. EXAMPLE:
With two outs, the pitcher is on second base and the catcher is on third base. The offense elects to use courtesy
runners for both the pitcher and catcher. The player who made the second out of the inning becomes the
courtesy runner for the pitcher at second base. The player who made the first out of the inning (due up
soonest) becomes the courtesy runner for the catcher at third base.

7.15 Procedures for Use of a Double First Base: The double base may be used for first base only. The base must be
rectangular, with two sides not less than 14 inches and not more than 15 inches, and the other two sides not less
than 29 inches and not more than 30 inches. The longer sides shall face toward home plate and the right field
corner. The outer edges shall not be more than two and one-fourth (2¼) inches thick, filled with soft material, and
covered with canvas or rubber. Half the base shall be white (entirely over fair territory) and half shall be orange or
green (entirely over foul territory). When using the double first base, the following rules must be observed:
(a) A batted ball that hits the white section of the double base shall be declared fair. A batted ball that hits the
colored (orange or green) section without first touching or bounding over the white section shall be declared
foul.
(b) Whenever a play is being made on the batter-runner, the defense must use the white section of the double
