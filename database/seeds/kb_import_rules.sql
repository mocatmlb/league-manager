f. A player cannot pitch in two games in a day. (EXCEPTION: Junior League – If a pitcher
reaches 30 pitches while facing a batter in the first game, the pitcher may continue to pitch, and
maintain their eligibility to pitch in the second game on that day, until any one of the following
conditions occurs: (1) that batter reaches base; (2) that batter is retired; (3) the third out is
made to complete the half inning or the game; or (4) the pitcher is removed from the mound
prior to the batter completing his/her at-bat. The pitcher would be allowed to pitch in a second
game provided that pitcher is moved, removed, or the game is completed before delivering a
pitch to another batter. If a player delivers 31 or more pitches in the first game, and is not
covered under the threshold exception, the player may not pitch in the second game that day.
League age 12-year-olds participating in the Junior Division are not eligible to pitch in two
games in a day.);
g. In a game suspended by darkness, weather, or other causes and resumed the following calendar day, the
pitcher of record at the time the game was halted may continue to the extent of his/her eligibility, provided he/
she delivered 40 or less pitches, and subject to each of these conditions:
1. If the pitcher delivered 20 or less pitches before the game was suspended, that pitcher’s pitch count will
begin at zero for the continuation portion of the game.
2. If the pitcher delivered between 21 and 40 pitches before the game was suspended, that pitcher’s pitch
count will begin with the number of pitches delivered in that game.
h. In a game (“Game A”) suspended by darkness, weather, or other causes and resumed more than one
calendar day later, the provisions of (g) above shall apply, unless the pitcher of record pitched in another game
or games after Game A was halted. In that event, eligibility to pitch in the continuation portion of Game A shall
be determined by the number of pitches delivered in the game or games after Game A was halted.
i. Failure to remove a pitcher who has reached his/her maximum number of pitches required by league age or
use of an ineligible pitcher is basis for protest. Violations protested or brought to the Tournament Committee’s
attention, shall result (by action of the Tournament Committee) in the suspension of the team’s manager for
the next two scheduled tournament games, even if those games are played at the next tournament level.
Additional penalties (up to and including forfeiture of a game and/or disqualification of the team, managers, or
coaches from further tournament participation) may be imposed if, in the opinion of the Tournament
Committee:
1. a manager or coach takes any action that results in making a travesty of the game, or;
2. a team fails to meet the requirements of this rule more than once during the International Tournament,
which begins with District play and ends at the World Series level (State level for 8- to 10- and 9- to 11-), or;
3. a manager willfully and knowingly disregards the requirements of this rule.
A manager or coach suspended for any reason is not permitted to be at the game site and must not take any
part in the game, nor have any communications whatsoever with any persons at the game site. This
includes pregame and postgame activities. Violation may result, by action of the Tournament Committee,
in further suspension; forfeiture of a game; and/or disqualification of the team, managers, or coaches from
further tournament participation.

