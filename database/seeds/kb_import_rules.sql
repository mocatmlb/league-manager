11. SUSPENDED GAMES: Any game in which a winner cannot be determined in accordance with the playing
rules shall be resumed from the exact point at which it was suspended regardless of the number of innings played.
Incomplete (not regulation) or tie games are considered suspended games. NOTE: A contest decided by forfeit
does not constitute a “game” for the purposes of this rule, unless one complete inning was physically played before
the game was forfeited. (Forfeits are only by decree of the Tournament Committee in Williamsport.)
12. RUN RULE: If at the end of three (3) innings [Intermediate (50-70) Division/Junior/Senior League:
four innings], two and one-half innings [Intermediate (50-70) Division/Junior/Senior League: three and
one-half innings] if the home team is ahead, one team has a lead of fifteen (15) runs or more, the manager of the
team with the least runs shall concede the victory to the opponent. If at the end of a regulation game one team has
a lead of ten (10) runs or more, the manager of the team with the least runs shall concede the victory to the
opponent. NOTE 1: If the visiting team has a lead of fifteen (15) or ten (10) or more runs respectively, the home
team must bat in its half of the inning. NOTE 2: A game determined by the 15-run or 10-run rule, shall be
considered a regulation game. NOTE 3: There is no 8-run rule in Tournament Play, as outlined in Rule 4.10(e) for
Regular Season only.

13. REGULATION GAME: Each tournament game must be played to the point of being an official game:
a. Regulation games are of four or more innings [five or more innings for Intermediate (50-70) Division/
Junior/Senior League] in which one team has scored more runs than the other [three and one-half (3½) if
the home team is ahead or four and one-half (4½) if the home team is ahead in Intermediate (50-70)
Division/Junior/Senior League].
b. Regulation games (when a winner can be determined) terminated because of weather, darkness, or curfew
must be resumed if the visiting team ties the game or takes the lead in its half of the inning and the home team
does not complete its at bat or take the lead in an incomplete inning. This does not apply to games suspended
or delayed by weather that may still be resumed before darkness or curfew (as defined in Tournament Rules
and Guidelines – Curfew) on the same day.
c. If two games are scheduled for the same site, no “time limit” may be imposed on the first game.
14. TIE GAME: When the completion of six innings [Intermediate (50-70) Division/Junior/Senior
League: seven innings], and the score is tied, starting in the top of the seventh inning [Intermediate (50-70)
Division/Junior/Senior League: eighth inning], and each half inning thereafter, the offensive team shall begin
its turn at bat with the player who is scheduled to bat last in that respective half inning being placed on second
base. EXAMPLE: If the number five batter is the lead-off batter, the number four batter in the batting order will
be placed on second base. An eligible substitute or special pinch runner may be inserted for the runner.
15. REPLAYING GAMES: No tournament game may be replayed without specific approval from the
