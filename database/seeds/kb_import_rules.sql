6. BENCH/DUGOUT: No one except the players, manager, and coach(es) shall occupy the bench or dugout
during a game. Base coaches may be players or adults. Two (2) adult base coaches are permitted at all levels subject
to playing rule 4.05(b).
7. VISITS: A manager or coach may not leave a dugout for any reason during a game without receiving permission
from an umpire. The manager or coach may be removed from the field for the remainder of the game for violation
of this rule. When permission is granted, the manager or coach will be permitted to go to the mound to confer with
the pitcher or any defensive player(s). A manager or coach who is granted a time out to talk to any defensive player
will be charged with a visit to the pitcher. 9- to 11-Year-Old Division, Little League, Intermediate (50-70)

Division, Junior League, and Senior League: A manager or coach may come out once in one inning to visit
with the pitcher, but the second time out, the player must be removed as a pitcher. The manager or coach may
come out twice in a game to visit with the pitcher, but the third time out, the player must be removed as a pitcher.
The rule applies to each pitcher who enters a game. 8- to 10-Year-Old Division: A manager or coach may come
out twice in one inning to visit with the pitcher, but the third time out, the player must be removed as a pitcher.
The manager or coach may come out three times in a game to visit with the pitcher, but the fourth time out, the
player must be removed as a pitcher. The rule applies to each pitcher who enters a game. NOTE: Only one
offensive time-out will be permitted each inning.
8. INJURY/ILLNESS: If a player is injured or becomes ill during a game, the decision of a doctor (if present) or
medical personnel will be final as to whether or not the player may continue in the game.
9. MANDATORY PLAY – CONTINUOUS BATTING ORDER: 8- to 10-Year-Old, 9- to 11-Year-Old,
Little League, Intermediate (50-70) Division, and Junior League: All Tournament Teams must
adopt a policy of a continuous batting order that will include all players on the team affidavit
present at the start of the game, to appear in the batting order. Each player is required to bat in
his/her respective spot in the batting order.
a. A player may be entered and/or re-entered defensively in the game at any time. Tournament Rule 4 (c)
provisions apply: 8-to 10-Year Old Division, 9-to 11-Year Old Division, Little League: Pitchers once
removed from the mound, may not return as pitchers. Intermediate (50/70) and Junior Division: A
pitcher remaining in the game defensively, but moving to a different defensive position, can return as a pitcher
anytime in the remainder of the game, but only once per game.
b. If a player is injured, becomes ill, or must leave the game site after the start of the game, the team will skip
over him/her when his/her time at bat comes up without penalty. If the injured, ill, or absent player returns,
he/she is merely inserted into their original spot in the batting order and the game continues. Also, if a player
arrives later to a game site, if the manager chooses to enter him/her in the lineup (see Rule 4.01 NOTE 2), he/
she would be added to the end of the current lineup.
c. An improper batter will be considered as batting out of turn (see Rule 6.07).
d. If a player is unable to complete a plate appearance due to injury, illness or ejection, the next batter in the
lineup shall take his/her place at the plate and assume the count of the original batter.
e. If a batter becomes a runner by reaching base safely and is unable to run the bases due to injury, illness, or
ejection, he/she shall be replaced by the player who recorded the last out or with a Courtesy Runner if
applicable (see Tournament Rule 3 (d) Courtesy Runner).
f. There is no exception to this rule. This rule does not apply to Senior Divisions of play.
NOTE: The rule replaces Regulation IV(i) and Rule 3.03.
