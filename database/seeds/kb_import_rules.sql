pitching regulations. Violation of these pitching rules is subject to protest and action by the

Tournament Committee in Williamsport if protested or brought to the Tournament Committee’s
attention.
a. Any player on a tournament team may pitch. Exception: Any player, who has played the position of catcher
in four (4) or more innings in a game, is not eligible to pitch on that calendar day. A player who played the
position of catcher for three (3) innings or less, moves to the pitcher position, and delivers 21 pitches or more
in the same day, may not return to the catcher position on that calendar day. EXCEPTION: If the pitcher
reaches the 20-pitch limit while facing a batter, the pitcher may continue to pitch, and maintain their eligibility
to return to the catcher position, until any one of the following conditions occur: (1) that batter reaches base;
(2) that batter is retired; (3) the third out is made to complete the half-inning or the game; or (4) the pitcher is
removed from the mound prior to the batter completing his/her at-bat.
b. A tournament pitcher may not pitch in regular season or Special Games while the team the player is a
member of is still participating in the tournament.
c. Pitchers once removed from the mound may not return as pitchers. Intermediate (50-70) Division/
Junior League: A pitcher remaining in the game defensively, but moving to a different defensive position,
can return as a pitcher anytime in the remainder of the game, but only once per game.
d. The manager must remove the pitcher when said pitcher reaches the pitch limit for his/her age group as
noted below, but the pitcher may remain in the game at another position:
