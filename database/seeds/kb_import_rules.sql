must be used. NOTE: Specific baseball models for tournament play can be found at LittleLeague.org/
TournamentBalls.
2. FIELDS: All fields are considered neutral. The home team shall be determined by the toss of a coin, the winner
having the choice.
3. PLAYING RULES: A copy or access to the Little League Rulebook App for Little League, Intermediate (50-70)
Division, Junior League, and Senior League Baseball Regulations and Playing Rules and the Tournament Rules
and Guidelines must be available at each tournament site and at the time the game is to be played. This is the
responsibility of the Tournament Director. Written ground rules established by the Tournament Director or
assistant must be reviewed with both managers and Umpire-in-Chief at least ten (10) minutes before the start of
the game. It is suggested the same be available to news media if requested.
a. After entering the batter’s box, the batter must remain in the box with at least one foot throughout the at bat.
EXCEPTIONS:

i. On a swing, slap, or check swing.

ii. When forced out of the box by a pitch.
iii. When the batter attempts a “drag bunt.”
iv. When the catcher does not catch the pitched ball.
v. When a play has been attempted.
vi. When time has been called.
vii. When the pitcher leaves the dirt area of the pitching mound or takes a position more than five feet from the
pitcher’s plate after receiving the ball or the catcher leaves the catcher’s box.
viii. On a three ball count pitch that is a strike that the batter thinks is a ball. PENALTY: If the batter leaves
the batter’s box or delays play and none of the exceptions apply, the umpire shall warn the batter. After one
warning on a batter, the umpire shall call a strike. Any number of strikes can be called on each batter. Minor/
Major Division: No pitch has to be thrown, the ball is dead, and no runners may advance. Intermediate
(50-70) Division/Junior/Senior: No pitch has to be thrown and ball is live. NOTE: The batter may return
their position in the batter’s box and assume the new count at any time during the at-bat, unless such enforced
penalty is the third strike.
b. In all Tournament levels and divisions, a batter is out for illegal action when the batter enters the batter’s
box with one or both feet entirely on the ground with an illegal bat (see bat specifications Rule 1.10) or is
discovered having used an illegal bat prior to the next player entering the batter’s box. The ball is dead.
Runners must return if they advanced on the play. NOTE: If the infraction is discovered before the next player
enters the batter’s box following the turn at bat of the player who used an illegal bat:
1. The ball is dead, the batter is out, and runners must return if they advanced on the play. The manager of
the defense may advise the plate umpire of a decision to decline this part of the penalty and accept the play.
Such election shall be made immediately at the end of the play.
2. The manager of the team will be ejected from the game, the batter who violated the rule will be ejected
from the game, and the offensive team will lose one eligible base coach for the duration of the game. A.R. –
Any of the remaining adults in the dugout may be used as the one adult base coach at any time during the

duration of the game. A.R. – When an illegal bat is discovered, it MUST be removed from the game at that
point.
c. 9- to 11-Year-Old Division: The batter may advance on an uncaught third strike (6.05/6.09). This rule
will not apply for the 8- to 10-Year-Old Division.
d. 8- to 10-Year-Old, 9- to 11-Year-Old, Little League, Intermediate (50-70) Division, and Junior
League: COURTESY RUNNER: A tournament team may permit a “courtesy runner” for the catcher and/or
pitcher of record when there are two (2) outs. Utilizing continuous batting order, the courtesy runner may be in
the team’s batting order and must be the player in the batting order who made the last out. Senior Division:
Twice a game but not more than one time per inning, a team may utilize a player who is not in the batting order
as a special pinch-runner for any offensive player. A player may only be removed for a special pinch-runner one
time during a game. The player for whom the pinch-runner runs is not subject to removal from the lineup. If
the pinch-runner remains in the game as a substitute defensive or offensive player, the player may not be used
again as a pinch-runner while in the batting order. However, if removed for another substitute that player or
any player not in the line-up, is again eligible to be used as a pinch-runner. A.R. 1 – The courtesy runner must
always be the player in the batting who made the last out. If a team chooses to use a courtesy runner and the
player who made the last out is the pitcher or catcher of record, that pitcher or catcher of record must be used
as the courtesy runner. A team may not skip the pitcher or catcher of record if that pitcher or catcher of record
made the last out and is therefore due to be the courtesy runner. A.R. 2 – If the pitcher and catcher of record
are on base with two outs, the offense may use a courtesy runner for both the pitcher and catcher of record at
the same time. When assigning courtesy runners in such situations, the player who is scheduled to bat soonest
shall be placed on the base closest to scoring. EXAMPLE: With two outs, the pitcher is on second base and the
catcher is on third base. The offense elects to use courtesy runners for both the pitcher and catcher. The player
who made the second out of the inning becomes the courtesy runner for the pitcher at second base. The player
who made the first out of the inning (due up soonest) becomes the courtesy runner for the catcher at third
base.
e. Each umpire has authority to disqualify any player, coach, manager, or substitute for objecting to decisions
or for unsportsmanlike conduct or language and to eject such disqualified person from the playing field. If an
umpire disqualifies a player while a play is in progress, the disqualification shall not take effect until no further
action is possible in that play. The stealing and relaying of signs to alert the batter of pitch selection and/or
location is unsportsmanlike behavior. If, in the judgment of the umpire this behavior is occurring, the team
manager and those responsible including any player(s) and/or coach(es) shall be warned after the first offense.
Those responsible including any player(s), coach(es), and/or manager shall be ejected from the game for a
team’s subsequent offense.
f. Managers or coaches are not permitted to warm up a pitcher at home plate or in the bullpen or elsewhere at
any time. They may stand by to observe a pitcher during warm-up in the bullpen.
g. Umpires are not required to inspect equipment prior to the start of the game. The managers of both teams
will confirm with the umpire(s) at the plate conference that all equipment is proper and legal according to
Little League Rules. PENALTY: If illegal equipment is used during the game, the manager of the team will be
removed for the remainder of the International Tournament, the player who used the illegal equipment will be
ejected from the game.
