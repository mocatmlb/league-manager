Rule 8.00 – The Pitcher
8.01 Legal pitching delivery. There are two legal pitching positions, the Windup Position and the Set Position, and
either position may be used at any time. Pitchers shall take signs from the catcher while standing on the pitcher’s
plate. Pitchers may disengage the pitcher’s plate after taking their signs but may not step quickly onto the pitcher’s
plate and pitch. This may be judged a quick pitch by the umpire. When the pitcher disengages the pitcher’s plate,
he/she must drop the hands to the sides.
(a) The Windup Position. The pitcher shall stand facing the batter, the pivot foot in contact with the pitcher’s
plate, and the other foot free. From this position any natural movement associated with the delivery of the ball
to the batter commits the pitcher to pitch without interruption or alteration. The pitcher shall not raise either
foot from the ground, except that in the actual delivery of the ball to the batter, said pitcher may take one step
backward, and one step forward with the free foot. From this position the pitcher may:
(1) deliver the ball to the batter, or
(2) step and throw to a base in an attempt to pick-off a runner, or
(3) disengage the pitcher’s plate. In disengaging the pitcher’s plate, the pitcher must step off with the pivot
foot and not the free foot first. The pitcher may not go into a set or stretch position. If the pitcher does, it is
an illegal pitch [a balk in Intermediate (50-70) Division/Junior/Senior League].
NOTE: When a pitcher holds the ball with both hands in front of the body, with the pivot foot in contact
with the pitcher’s plate, and the other foot free, that pitcher will be considered in a Windup Position.
(b) The Set Position. Set Position shall be indicated by the pitcher when that pitcher stands facing the batter
with the pivot foot in contact with, and the other foot in front of, the pitcher’s plate, holding the ball in both
hands in front of the body and coming to a complete stop. From such Set Position the pitcher may deliver the
ball to the batter, throw to a base, or step backward off the pitcher’s plate with the pivot foot. Before assuming
the Set Position, the pitcher may elect to make any natural preliminary motion such as that known as “the
stretch.” But if the pitcher so elects, that pitcher shall come to the Set Position before delivering the ball to that
batter. NOTE: In Little League (Major) and below the pitcher need not come to a complete stop.
Intermediate (50-70) Division/Junior/Senior League: the pitcher must come to a complete and
discernible stop.
(c) At any time during the pitcher’s preliminary movements and until the natural pitching motion commits
that pitcher to the pitch, said pitcher may throw to any base provided the pitcher steps directly toward such
base before making the throw. The pitcher shall step “ahead of the throw.” A snap throw followed by the step
toward the base is an illegal pitch [a balk in Intermediate (50-70) Division/Junior/Senior League].
(See Penalty for illegal pitch/balk under Rule 8.05.)
(d) If the pitcher makes an illegal pitch with the bases unoccupied, it shall be called a ball unless the batter
reaches first base on a hit, an error, a base on balls, a hit batter, or otherwise. A ball which slips out of the
pitcher’s hand and crosses the foul line shall be called a ball; otherwise it will be called “no pitch” without
runners on base, and an illegal pitch [a balk in Intermediate (50-70) Division/Junior/Senior League]
with runners on base. (See Penalty for illegal pitch under Rule 8.05.)
(e) If the pitcher removes the pivot foot from contact with the pitcher’s plate by stepping backward with that
foot, that pitcher thereby becomes an infielder and in the case of a wild throw from that position, it shall be
considered the same as a wild throw by any other infielder.
(f) A pitcher must indicate visually to the Umpire-in-Chief, the batter, and any runners the hand with which
he/she intends to pitch, which may be done by wearing his/her glove on the other hand while touching the

pitcher’s plate. The pitcher is not permitted to pitch with the other hand until the batter is retired, the batter
becomes a runner, the inning ends, the batter is substituted for by a pinch-hitter, or the pitcher incurs an
injury. In the event a pitcher switches pitching hands during an at-bat because he/she has suffered an injury,
the pitcher may not, for the remainder of the game, pitch with the hand from which he/she has switched. The
pitcher shall not be given the opportunity to throw any preparatory pitches after switching pitching hands. Any
change of pitching hands must be indicated clearly to the Umpire-in-Chief.
(g) Tee Ball: The pitcher shall keep both feet on the pitcher’s plate until the ball is hit.
8.02 The pitcher shall not –
(1) bring the pitching hand in contact with the mouth or lips while in contact with the pitcher’s
plate. EXCEPTION: The pitcher may bring the hand in contact with the mouth or lips while in the 10-foot
circle (Intermediate (50-70) Division/Junior/Senior: 18-foot circle) surrounding the pitcher’s plate
provided he/she distinctly wipes off the pitching hand before contacting the ball. PENALTY: For violation
of this part of the rule the umpires shall immediately call a ball and warn the pitcher that repeated violation
of any part of this rule can cause the pitcher to be removed from the game. However, if the pitch is made
and a batter reaches first base on a hit, an error, a hit batter, or otherwise, and no other runner is put out
before advancing at least one base, the play shall proceed without reference to the violation.
(2) apply a foreign substance of any kind to the ball;
(3) expectorate on the ball, either hand or the glove;
(4) rub the ball on the glove, person, or clothing;
(5) deface the ball in any manner; or
(6) deliver what is called the “shine” ball, “spit” ball, “mud” ball, or “emery” ball. The pitcher is allowed to
rub off the ball between the bare hands;
PENALTY: For violation of any part of Rules 8.02(a)(2) through (6) the umpire shall: Call the pitch a ball
and warn the pitcher. If a play occurs on the violation, the manager of the offense may advise the plate
umpire of acceptance of the play. (Such election must be made immediately at the end of play.) NOTE: A
pitcher may use a rosin bag for the purpose of applying rosin to the bare hand or hands. Neither the pitcher
nor any other player shall dust the ball with the rosin bag; neither shall the pitcher nor any other player be
permitted to apply rosin from the bag to their glove or dust any part of the uniform with the rosin bag.
(b) Intentionally delay the game by throwing the ball to players other than the catcher, when the batter is in
position, except in an attempt to retire a runner, or commit an illegal pitch for the purpose of not pitching to
the batter (i.e. intentional walk, etc.) PENALTY: If, after warning by the umpire, such delaying action is
repeated, the pitcher can be removed from the game.
(c) Intentionally pitch at the batter. If in the umpire’s judgment, such violation occurs, the umpire shall warn
the pitcher and the manager of the defense that another such pitch will mean immediate expulsion of the
pitcher. If such pitch is repeated during the game, the umpire shall eject the pitcher from the game.
8.03 When a pitcher takes a position at the beginning of each inning, that pitcher shall be permitted to pitch not to
exceed eight preparatory pitches to the catcher, or other teammate acting in the capacity of catcher, during which
play shall be suspended. Such preparatory pitches shall not consume more than one minute of time. If a sudden
emergency causes a pitcher to be summoned into the game without any opportunity to warm up, the Umpire-inChief shall allow the pitcher as many pitches as the umpire deems necessary.
8.04 When the bases are unoccupied, the pitcher shall deliver the ball to the batter within 20 seconds after the
pitcher receives the ball. Each time the pitcher delays the game by violating this rule, the umpire shall call “Ball.”

NOTE: The intent of this rule is to avoid unnecessary delays. The umpire shall insist that the catcher return the
ball promptly to the pitcher, and that the pitcher take a position on the pitcher’s plate promptly.
