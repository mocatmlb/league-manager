Curfew
No inning shall start after midnight prevailing time [12:30 a.m. prevailing time for Intermediate (50-70)
Division/Junior League; 1:00 a.m. prevailing time for Senior League].
NOTE 1: An inning starts the moment the third out is made completing the previous inning.
NOTE 2: Neither Tournament Directors and officials nor tournament teams are permitted to circumvent the
curfews established above by continuing, suspending and restarting, or starting a game after curfew has been
reached and play is required to be terminated. If the curfew noted above occurs during a game suspended in

accordance with Tournament Playing Rule 11, that game must not be continued after the curfew. It must either
be resumed on a subsequent day, or declared ended, as determined by rule.
Starting Time of Games
A game shall not be started unless the Tournament Director or assistant judges there is adequate time to
complete the game before darkness or curfew.
Admission Charge
There shall be no charge for admission to Little League, 8- to 10-Year-Old, or 9- to 11-Year-Old Division
Tournament games. An admission charge is permitted for Intermediate (50-70) Division/Junior/Senior
League.

Conditions of Tournament Play
Protests
This rule replaces Rule 4.19.
No protest shall be considered on a decision involving an umpire’s judgment. Equipment which does not meet
specifications must be removed from the game.
Protest shall be considered only when based on:
A. The violation or interpretation of a playing rule; When a manager claims that a decision is in violation
of the playing rules, the following steps must be taken:
1. A formal (verbal) protest must be made to the Umpire-in-Chief at once by the manager or coach.
2. The Umpire-in-Chief must immediately call a conference of all umpires working the game.
3. If the problem cannot be resolved to the satisfaction of the managers, the Umpire-in-Chief shall be required
to consult with the Tournament Director or District Administrator.
4. If the managers do not accept the decision of the Tournament Director, either manager may elect, without
penalty, to discontinue play until the matter is referred to the Regional Headquarters. Either the Umpire-inChief, Tournament Director or District Administrator will call the Regional Headquarters at this time.
5. If the managers do not accept the decision of the Regional Director (or his/her designated agent), either may
insist that the matter be referred to the Tournament Committee in Williamsport. The decision of the
Tournament Committee shall be final and binding.
NOTE 1 – PROTESTS INVOLVING PLAYING RULES NOT RESOLVED BEFORE THE NEXT PITCH OR
PLAY SHALL NOT BE CONSIDERED. NOTE 2 – UMPIRES, TOURNAMENT DIRECTORS, AND DISTRICT
ADMINISTRATORS DO NOT HAVE THE AUTHORITY TO DECLARE A FORFEITURE UNDER ANY
CIRCUMSTANCES.
B. The use of an ineligible pitcher. Ineligibility under this rule applies to violations of Tournament Playing
Rule 4. If an ineligible pitcher delivers one or more pitches to a batter, that game is subject to protest and action by
Tournament Committee in Williamsport.
1. If the facts establishing or verifying an ineligible pitcher become known DURING a game, and the ineligible
pitcher participates in the game, subject to the following conditions:

(a) A protest may be lodged by the manager or coach with the Umpire-in-Chief, who shall consult with the
Tournament Director or District Administrator.
(b) The Tournament Director or District Administrator must contact the Regional Director (or his/her
appointed agent), who shall contact the Tournament Committee for a decision. The decision of the
Tournament Committee shall be final and binding.
2. If the facts establishing or verifying an ineligible pitcher become known AFTER a game, and the ineligible
pitcher participated in the game, subject to the following conditions:
(a) A protest may be lodged by the manager or coach with the Tournament Director or District
Administrator. Such protest must be made before either team affected by the protest begins another game.
(b) The Tournament Director or District Administrator must contact the Regional Director (or his/her
appointed agent), who shall contact the Tournament Committee for a decision. The decision of the
Tournament Committee shall be final and binding.
PENALTY: See Tournament Rule 4(i).
C. The use of an ineligible player. Ineligibility under this rule applies to league age, residence or school
attendance (as defined by Little League Baseball, Incorporated), participation in other programs, or participation
as an eligible player for eight (8) [Senior: No minimum regular season games required] games during the regular
season in the proper division.
1. If the facts establishing or verifying the ineligibility of a player are known to the complainant, Tournament
Official, or Tournament Director after the ineligible player participated in a game during the International
Tournament, that team shall be disqualified and removed from the International Tournament, subject to any of
the following conditions:
(a) A protest is lodged by the manager or coach with the Umpire-in-Chief, who shall consult with the
Tournament Director or District Administrator. The Tournament Director or District Administrator must
contact the Regional Director (or his/her appointed agent), who shall contact the Tournament Committee
for a decision. The decision of the Tournament Committee shall be final and binding.
(b) The Tournament Director, District Administrator, or Tournament Official become aware of the
circumstances by any means, they must contact the Regional Director (or his/her appointed agent), who
shall contact the Tournament Committee for a decision. The decision of the Tournament Committee shall
be final and binding.
In addition to the penalties described above in A, B, and C, the Tournament Committee may disqualify a player,
team, or entire league from tournament play. The Tournament Committee may take action as a result of a protest
or on its own initiative. Disqualification of a team or player(s) and/or forfeiture of a game must be the decision of
the Tournament Committee at Williamsport, and such decisions will be made prior to the continuation of the
affected team(s) or player(s) in further tournament play. NOTE: All officials, including all managers, coaches,
scorekeepers, umpires, Tournament Directors, District Administrators, etc., should make every effort to prevent a
situation that may result in the forfeiture of a game or suspension of tournament privileges. However, failure by
any party to prevent such situations shall not affect the validity of a protest. Must Play To Advance A team shall
not advance from one level of Tournament to a higher level of tournament play without first having competed
against and defeated a scheduled opponent at the tournament level from which it is seeking to advance. Any team
advancing without play must do so with the approval of the District Administrator, who must certify that the
advancing team has participated in a Little League regular season schedule of games against other Little League
teams and has met all other requirements to establish eligibility for tournament play.

