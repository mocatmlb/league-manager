League Eligibility
In order for a Little League program to be eligible to enter a team or teams into the International Tournament
(including 8- to 10-Year-Old Division and 9- to 11-Year-Old Division) the following must be accomplished as
indicated:
1. The league must be chartered in an age appropriate division(s) for which it wishes to enter a tournament
team(s), no later than June 1, 2026.
2. The league must have scheduled and played, at a minimum, a 12-game [Senior League: No minimum regular
season games required] (per team) regular season, exclusive of playoffs and tournament games, for each
division, or age appropriate division, entering tournament prior to the first game in their respective tournament.
See Reg. VII.
NOTE 1: Forfeited games shall not count towards the 12-game [Senior League: No minimum regular season
games required] regular season.
NOTE 2: For Junior Tournament eligibility, Senior League players league age 12, 13 and 14 must have played in
a minimum of eight (8) Regular Season games.
3. All waiver requests (for the league, team, player, manager, and/or coach) of any kind must be submitted and
approved not later than June 1, 2026.
4. Team number revisions and fees incurred by the league must be paid in full by June 1, 2026.
5. All combined team and interleague play requests that may involve tournament play must be submitted and
approved not later than June 1, 2026.
Failure to meet any of the listed requirements could result in a team or teams being declared ineligible by the
Tournament Committee at Little League International.
